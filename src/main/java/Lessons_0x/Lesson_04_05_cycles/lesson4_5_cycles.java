public class lesson4_5_cycles {
    public static void main(String[] args) {
        // Prints "Hello, World" in the terminal window.
        int a = 0;
        while (a < 10) {
            System.out.print(a);
            a++;
        }

        for (int ii = 0 ; ii<=10; ii=ii+1 ) {
            System.out.println(ii);
        }


    }}